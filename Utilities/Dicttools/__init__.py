# -------------------------------------------------------------------------------------------------
# ------------------------------------- Utilities :: Dicttools ------------------------------------
# -------------------------------------------------------------------------------------------------
from typing import TypeVar
from typing import Any

K = TypeVar('K')
V = TypeVar('V')

import collections


# -------------------------------------------------------------------------------------------------
# ------------------------- Utility :: Default Dictionary with Key Access -------------------------
# -------------------------------------------------------------------------------------------------
class DefaultDict(collections.defaultdict[K, V]):

	def __missing__(self, key: K) -> V:
		return self.setdefault(key, self.default_factory(key))
