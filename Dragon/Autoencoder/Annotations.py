# -------------------------------------------------------------------------------------------------
# ------------------------------ Dragon :: Autoencoder :: Annotations -----------------------------
# -------------------------------------------------------------------------------------------------
from typing import Annotated
from typing import TypeVar

T = TypeVar('T')


# -------------------------------------------------------------------------------------------------
# ----------------------------- Utility :: Schema Annotation Metaclass ----------------------------
# -------------------------------------------------------------------------------------------------
class Annotation(type):

	# -----------------------------------------------------------------------------------------
	# -------------------------------- Operator :: Constructor --------------------------------
	# -----------------------------------------------------------------------------------------
	def __new__(meta, name: str) -> 'Annotation':
		return super().__new__(meta, name, tuple(), dict())


Attr = Annotated[T, Annotation('Attr')]
Edge = Annotated[T, Annotation('Edge')]
Slot = Annotated[T, Annotation('Slot')]
Type = Annotated[T, Annotation('Type')]