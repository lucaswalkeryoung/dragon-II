from typing import Any
from typing import Iterator
from typing import Mapping

import collections


# -------------------------------------------------------------------------------------------------
# ----------------------------- Utility :: Embeddings Table Schematic -----------------------------
# -------------------------------------------------------------------------------------------------
class Schematic(type):

	# -----------------------------------------------------------------------------------------
	# -------------------------------- Operator :: Constructor --------------------------------
	# -----------------------------------------------------------------------------------------
	def __init__(self, name: str, bases: tuple[type, ...], namespace: dict[str, Any]) -> None:
		super().__init__(name, bases, namespace)

		self.schematic = collections.defaultdict(lambda: len(self.schematic))


	# -----------------------------------------------------------------------------------------
	# ---------------------------------- Operator :: Get Item ---------------------------------
	# -----------------------------------------------------------------------------------------
	def __getitem__(self, key: str) -> int:
		return self.schematic[key]